public class ChooserType
{
	private int id;
	/**
	 * 
	 * @param newId the id of the selection represented as an integer.
	 */
	public ChooserType(int newId)
	{
		id = newId;
	}
	
	/**
	 * 
	 * @return the id of the selection
	 */
	public int getId()
	{
		return id;
	}
}
